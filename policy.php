							How are Contract Tools and Turner similar?<br>

							Contract Tools and Turner both include the following features:<br>

							Navigation and management tools for provisions, defined terms, cross-references, related items, comments, and ancillaries
							Proofreading for common contract drafting problems;<br> ways to see problems in context and ignore problems that don’t bother you
							Automatic updating as you type and delete text<br>
							Defined term autocompletion<br>
							Ways to navigate a document by clicking its text; navigation history (Back and Forward buttons)<br>
							Ways to create cross-references that update automatically<br>
							Ways to easily make formatting in your document consistent<br>
							Enhanced search capabilities<br>
							How are Contract Tools and Turner different?<br>

							Here are some major differences between Contract Tools and Turner:

							Contract Tools is a Word add-in for Windows PCs. Turner is a standalone app for macOS.<br>
							Contract Tools analyzes the text of your document to find defined terms, cross-references, provisions, and other items. Turner finds these items when you import documents from other file formats, and also offers controls for creating these items.<br>
							You can export Turner documents to a number of formats, including DOCX, PDF, and HTML files suitable for the SEC’s EDGAR system. Also, some things that are time-consuming to create in Word, like an index of defined terms, can be included in exported files with a few clicks. Contract Tools doesn’t change Word’s export capabilities.<br>
							Do Contract Tools and Turner work with non–English language documents?

							Some features of Contract Tools and Turner work with documents in any language, but Contract Tools and Turner are best suited to documents in English.<br>

							Do Contract Tools and Turner send information over the internet?

							Contract Tools and Turner send information over the internet to begin a trial, to validate your license activation, and to check for updates. Turner asks whether you want to send diagnostic information over the internet if Turner quits unexpectedly.<br> Contract Tools and Turner never log or share any information about your documents. See our privacy statement to learn more.<br>

							Do Contract Tools and Turner work with PDFs?<br>

							Contract Tools works with PDFs that you open in Word, and you can open PDFs in Word 2013 and later. <br>Turner can import PDFs.<br>

							Do Contract Tools and Turner detect text (using OCR) in PDFs that lack text data?

							No. There are many apps available for detecting text in PDFs that lack text data. We use OCRmyPDF.<br> Search the internet for “ocr app” to learn about other apps.<br>

							Do Contract Tools and Turner offer features for comparing documents (running redlines and blacklines)?<br>

							No. There are many apps available for comparing documents. Search the internet for “compare documents” to learn more. To learn how to compare documents using Word for Windows PCs, visit Compare document differences using the legal blackline option; to learn how to do this using Word for Mac, visit Compare and merge two versions of a document.<br>

							How do I begin a free trial of Contract Tools?<br>

							Install Contract Tools, open a document or create a new document in Word to show the Contract Tools pane, and then click Begin Trial.<br> Visit Trying Contract Tools to learn more.<br>

							Can I and my colleagues try Contract Tools and Turner at our organization?<br>

							Yes.<br> We offer 3-month pilot deployments to organizations.<br> Email sales@papersoftware.<br>com to learn more.<br> The Contract Tools evaluation agreement is available here, and the Turner evaluation agreement is available here.<br>

							Can I use a Contract Tools activation key to activate Turner, or a Turner activation key to activate Contract Tools?<br>

							No.<br> Contract Tools and Turner are licensed separately.<br>

							Can I use an activation key to activate Contract Tools or Turner on more than one computer at the same time?<br>

							No.<br> If you have more than one computer, you can deactivate Contract Tools or Turner on one computer, and then reactivate it on another.<br> If you forget to deactivate Contract Tools or Turner on one of your computers, you can easily deactivate it on a computer that you don’t have access to.<br> As an alternative, you can buy subscriptions for each of your computers.<br> If there are many people in your organization who want to use Contract Tools or Turner, email sales@papersoftware.<br>com.<br>

							Do Contract Tools and Turner have Export Control Classification Numbers (ECCNs)?<br>

							Contract Tools and Turner are self-classified under ECCN 5D992.<br>

							Do you offer discounts to students?<br>

							We offer discounts to groups of students (like clinics, classes, or entire law schools).<br> We don’t offer discounts to individual students.<br> If you’d like to explore using Contract Tools or Turner in an academic environment, email sales@papersoftware.<br>com.<br>

							Do you offer discounts to solo practitioners?<br>

							No.<br>

							Do you offer volume discounts?<br>

							Yes, starting at 5 licenses.<br> Email sales@papersoftware.<br>com to learn more.<br>

							Does Contract Tools work with Word documents that don’t use formatting, automatic list numbering, and similar Word features?<br>

							Yes.<br> If your document includes formatting, automatic list numbering, and so on, Contract Tools analyzes these items to find information about your document.<br> But Contract Tools can also analyze unformatted text (like text you copy and paste from a PDF).<br>

							Can I use Contract Tools while using Track Changes?<br>

							Yes.<br> When you use Contract Tools and Track Changes together, you can add and delete text, show and hide revisions, and adjust other Track Changes settings as if you weren’t using Contract Tools.<br> Depending on whether you want Contract Tools to analyze tracked deletions, you may want to adjust Track Changes settings; visit Analyzing When Using Track Changes to learn more.<br>

							Does Contract Tools change Word documents?<br>

							Yes, in two ways.<br> By default, Contract Tools automatically detects the part of your document that you’re most likely to want to analyze, and then saves this information as a hidden bookmark; visit Analyzing Part of a Document to learn more.<br> Also, in Word 2010 and later, Contract Tools saves analysis settings with documents as custom XML data; visit Saving Analysis Settings to learn more.<br> It’s easy to turn these features off.<br>

							Do changes that Contract Tools makes to Word documents appear in comparisons (like redlines and blacklines)?<br>

							No.<br>

							Do changes that I make to Word documents while using Contract Tools appear in comparisons?<br>

							Yes.<br>

							Can I use Contract Tools on a virtual machine (like one provided by Citrix Virtual Apps and Desktops, Hyper‑V, Parallels Desktop, or VMware Fusion)?<br>

							Yes, but there’s a 5-license minimum, and it’s necessary to use Contract Tools License Server.<br> Contract Tools License Server is a lightweight app that’s compatible with Windows 7 or later and Windows Server 2008 or later.<br> Setting it up usually takes only a few minutes.<br> Visit Contract Tools License Server to learn more.<br>

							Does Contract Tools work with Microsoft 365?<br>

							You can use Contract Tools with the version of Word for Windows PCs that’s available with a Microsoft 365 plan.<br> Contract Tools isn’t available for the Office mobile app or Word Online.<br>

							Is Contract Tools compatible with Word for Mac?<br>

							No.<br> If you want to use Contract Tools on a Mac, consider using Boot Camp to install Windows on your Mac, and then using Contract Tools with Word for Windows PCs.<br> Or, consider using Turner.<br>
